# Solicitar el nombre del usuario
nombre_usuario = input("Por favor, introduce tu nombre: ")

# Convertir el nombre a mayúsculas
nombre_en_mayusculas = nombre_usuario.upper()

# Contar el número de letras en el nombre
numero_de_letras = len(nombre_usuario)

# Imprimir el resultado
print(f"{nombre_en_mayusculas} tiene {numero_de_letras} letras.")
